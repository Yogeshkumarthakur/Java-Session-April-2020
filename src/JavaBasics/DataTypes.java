package JavaBasics;

public class DataTypes {

	//main() method is starting or execution point of the program
	public static void main(String[] args) {
		
		//Primitive data type : int, double, char, boolean
		//1. int:
		int i = 10;
		i = 20;
		int j = 30;
		System.out.println(j);
		int k = 40;
		int l = -1;
		System.out.println(l);
		int p = 0;
		System.out.println(i+j);
		
		//2. double
		double d = 12.33;
		double d1 = 34.44;
		System.out.println(d1);
		double d2 = 100;  //this will be treated like 100.00
		
		//3. char: only Single digit value is allowed and only written under single quote
		char c =  'a';
		char c1 = 'A';
		char c2 = '1';
		char c3 = '$';
		
		//4. boolean: true and false are values but also keyword available on java
		boolean b1 = true;
		boolean b2 = false;
		
		//5. String: is a class not a data type and always written in double quote
		String s =  "Hello World";
		String s1 = "Selenium";
		String s2 = "This is my first java code";
		String s3 = "1000";
		String s4 = "12.33";
		System.out.println(s2);
		
		
		
		
		
	}

}
